/* ============================================================
 * 用户态程序入口汇编
 *
 * 放在 .text.entry 节，链接脚本保证它位于程序最开头。
 * 内核跳转到 BASE_ADDRESS 时即执行此处。
 * ============================================================ */

    .section .text.entry
    .global _start
_start:
    /* 内核已在 sp 中设置好用户栈顶，直接调用 Rust main */
    bl      main
    /* main 返回后调用 sys_exit(0) */
    mov     x0, #0
    mov     x8, #93        /* SYS_EXIT = 93 */
    svc     #0
    /* sys_exit 返回后进入无限循环 */
1:  b       1b
